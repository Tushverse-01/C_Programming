void DisplayFiveToOne()
{
    int i=0;            //  To Store the Result

        i=5;            //  Reinitialization

    while(i!=0)         //  Iterration
    {
        printf("%d \n",i);

        i--;            //  Decrement
    }
}   //  End of DisplayFiveToOne Function
    //  Time Complexity: O(N)
    
///////////////////////////////////////////////////////
/////////////////////////
//
//  Entry point Function for Application
//
/////////////////////////
///////////////////////////////////////////////////////

int main()
{

    DisplayFiveToOne();

    return 0;

}   //  End of Main Function

